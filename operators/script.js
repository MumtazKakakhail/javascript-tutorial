// Arithmatic Operators
let a = 11;
b = 13;
console.log("a =", a, "& b =", b);

console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);

//Unary operators
let c = 5;
d = 7;
console.log("c =", c, "d =", d);
c++;
console.log("c =", c);
d--;
console.log("d =",d);
++c;
console.log("++c =", ++c);

//Assignment Operators
//= , += , -= , *= , %= , **= , /=
let x = 3;
x += 4;
console.log("x =", x);
x -= 2;
console.log("x =", x);
x *= 5;
console.log("x =", x);
x %= 4;
console.log("x =", x);
x **= 2;
console.log("x =", x);
x /= 4;
console.log("x =", x);

